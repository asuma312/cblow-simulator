import type { AITeam, Player } from '@/types/game.types'

function makePlayer(
    id: string, name: string, nickname: string, role: 'top' | 'jungle' | 'mid' | 'adc' | 'support',
    farm: number, mechanics: number, teamfight: number,
    champPool: { championId: string; knowledge: number }[],
    salary: number, popularity: number
): Player {
    return {
        id, name, nickname, role,
        stats: { farm, mechanics, teamfight },
        champPool,
        salary, popularity, moral: 75, fatigue: 20,
    }
}

